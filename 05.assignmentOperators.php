<?php
    // Assignment Operators

    $a = 5; //Assining value in variable
    echo "= $a <br>";
    $a += 2; //Adding value in same variable, variable will increase its latest value by current value
    echo "+= $a <br>";
    $a -= 4; //Subtructing values in same variable, variable will decrease its latest value by current value
    echo "*-= $a <br>";
    $a *= 2;
    echo "*= $a <br>";
    $a /= 2;
    echo "/= $a <br>";
    $a %= 2;
    echo "%= $a <br>";
?>